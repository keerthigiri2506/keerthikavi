package com.java.demo;

public class Logical_Operators {
	public static void main(String[] args) {
		int a = 10;
		String S = "Welcome";
		if (a==5 && S=="Welcome"){
				System.out.println("Condition Satisfied");
					}
		
		else if (a==5 || S=="Welcome") {
			System.out.println("True");
		}
		
		else {
			System.out.println("False");
		}
	}

}
